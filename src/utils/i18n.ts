const dictionaries: Record<AppLanguage, Dictionary> = {
  en: english,
  el: greek
};

export interface TranslationIssue {
  language: AppLanguage;
  key: string;
  reason: "missing" | "empty";
}

const warnedKeys = new Set<string>();

export const getTranslationIssues = (): TranslationIssue[] => {
  const keys = Object.keys(english);
  const issues: TranslationIssue[] = [];
  for (const language of Object.keys(dictionaries) as AppLanguage[]) {
    for (const key of keys) {
      const value = dictionaries[language][key];
      if (value === undefined) {
        issues.push({ language, key, reason: "missing" });
      } else if (value.trim() === "") {
        issues.push({ language, key, reason: "empty" });
      }
    }
  }
  return issues;
};

export const checkMissingTranslations = () => getTranslationIssues();

export const reportTranslationIssuesInDevelopment = () => {
  if (!import.meta.env.DEV) {
    return;
  }
  const issues = getTranslationIssues();
  if (issues.length > 0) {
    console.warn("[Fit Quest i18n] Translation issues", issues);
  }
};

export const translate = (language: AppLanguage, key: string) => {
  const value = dictionaries[language][key];
  if (value !== undefined && value.trim() !== "") {
    return value;
  }
  const fallback = english[key] ?? key;
  if (import.meta.env.DEV) {
    const warningKey = `${language}:${key}`;
    if (!warnedKeys.has(warningKey)) {
      warnedKeys.add(warningKey);
      console.warn(`[Fit Quest i18n] Missing ${language} translation for "${key}"`);
    }
  }
  return fallback;
};

export const getAdventureLanguage = (settings?: Settings): AppLanguage => {
  const appLanguage = settings?.appLanguage ?? "en";
  const adventureLanguage: AdventureLanguage = settings?.adventureLanguage ?? "same";
  return adventureLanguage === "same" ? appLanguage : adventureLanguage;
};

export const translateAdventure = (settings: Settings | undefined, key: string) =>
  translate(getAdventureLanguage(settings), key);
